§1 FIRMA - Förenings firma är Samfällighetsföreningen Trumpeten<p>
§2 SAMFÄLLIGHETER - Föreningen förvaltar anläggningssamfälligheter tillkomna genom anläggningsbeslut 1983-03-15 i förrättning dnr N3 191 81 7.<p>
§3 GRUNDERNA FÖR FÖRVALTNINGEN. - Samfälligheten(erna) skall förvaltas i enighet med vad som vid bildandet bestämts om dess (deras) ändamål.<p>
§4 MEDLEM - Medlem i föreningen är ägare till fastighet eller därmed jämställd egendom,
som har del i samfällighet upptagen under §2.<p>
§5 STYRELSE säte, sammansättning - För föreningen skall finnas en styrelse med säte i Kungsbacka kommun.
Styrelsen skall bestå av minst tre och högst fem ledarmöter och minst tre och högst fem suppleanter.
<p>
§6 STYRELSE val - Styrelsen väljs vid ordinarie föreningsstämma. <br>
Mandattiden för ledamot är två år och för suppleant ett år.<br>
Första gången val äger rum skall två ledamöter väljas på endast ett år.<br>
Stämman utser ordförande bland styrelsens ledarmöter. I övrigt konsituerar styrelsen sig själv.
<p>
§7 STYRELSE kallelse till sammanträde, föredragningslista<br> 
- Kallelse till styrelsesammanträde, vilken skall innehålla uppgift om förekommande ärenden skall tillställas ledarmöterna minst 10 dagar före sammanträdet.<br>
Suppleanterna skall inom samma tid tillställas underättelse om sammanträdet och förekommande ärenden.
Ledarmot som är förhindrad att närvara, skall genast meddela detta till ordföranden, som har att omdelbart kalla suppleant i ledarmotens ställe.
Suppleant som ej tjänstgör i ledarmotens ställe har rätt att närvara vid sammanträdet men har ej rösträtt.
<p>
§8 STYRELSE beslutsförhet, protokoll. <br>
- Styrelsen är beslutför när kallelse har skett i behörig ordning och minst halva antalet styrelseledamöter är närvarande. Utan hinder av detta ska styrelsesammanträde anses behörigen utlyst om samtliga ordinarie ledamöter har infunnit sig till sammanträdet.<br>

Som styrelsens beslut gäller den mening som får flest röster. 
Vid lika röstetal gäller den mening som biträds av ordföranden, 
förutom vid personval där beslutet i så fall avgörs genom lottning.
 Ett ärende som inte har stått i kallelsen får avgöras om minst två tredjedelar av styrelseledamöterna är närvarande och överens om beslutet. En fråga får avgöras utan att kallelse har skett om samtliga ordinarie styrelseledamöter är överens om beslutet.

Den som har deltagit i avgörandet av ett ärende får reservera sig mot beslutet. Sådan reservation ska anmälas före sammanträdets slut. Protokoll ska föras över de ärenden där styrelsen har fattat beslut. Protokollet ska innehålla datum, deltagande styrelseledamöter och suppleanter, en kort beskrivning av ärendet, styrelsens beslut samt anförda reservationer. Protokollet ska justeras av ordföranden eller av annan ledamot som har lett sammanträdet i stället för ordförande.
<p>
§9 Styrelse, förvaltning
<br>
Styrelsen ska:
<br>
    1 Förvalta samfälligheten och föreningens tillgångar.
<br>
    2 Föra redovisning över föreningens räkenskaper.
<br>
    3 Föra förteckning över delägande fastigheter, deras andelstal och ägare.
<br>
    4 Årligen till ordinarie föreningsstämma avge förvaltningsberättelse över föreningens verksamhet och ekonomi.
<br>
    5 Om förvaltningen omfattar flera samfälligheter eller annars är uppdelad på olika verksamhetsgrenar och medlemmarnas andelar inte är lika stora i alla verksamhetsgrenarna, föra särskild redovisning för varje sådan gren.
<br>
    6 I övrigt fullgöra vad lagen föreskriver om hur styrelsen ska sköta föreningens angelägenheter.
<p>
§10 Revision<br>

För granskning av styrelsens förvaltning ska medlemmarna på ordinarie föreningsstämma utse två revisoror och två suppleanter. 
Revisionsberättelse ska överlämnas till styrelsen senast tre veckor före ordinarie stämma.
<p>
§11 Räkenskapsperiod<br>

Föreningens räkenskapsperiod omfattar tiden 1 januari till 31 december.
<p>
§12 Föreningsstämma<br>
Ordinarie föreningsstämma ska årligen hållas under mars månad på tid och plats som styrelsen bestämmer. 
Styrelsen kan utlysa en extra föreningsstämma när den anser att det behövs. 
I fråga om medlemmars rätt att begära en extra föreningsstämma gäller 
47 § tredje stycket SFL. Om föreningsstämman ska godkänna uttaxering ska styrelsen ge medlemmarna 
tillfälle att från det att kallelsen har skett ta del av en debiteringslängd, 
som visar det belopp som ska uttaxeras, vad varje medlem ska betala och när betalning ska ske. 
Före ordinarie föreningsstämma ska dessutom en förvaltningsberättelse och en revisionsberättelse för den avslutade 
räkenskapsperioden samt en utgifts- och inkomststat finnas tillgänglig för granskning under samma tid.
<p>
§13 Kallelse till stämma, andra meddelanden<br>

Styrelsen kallar till föreningsstämma. Det ska ske genom personliga kallelser.

Kallelse ska ske senast 14 dagar före föreningsstämman. 
I kallelsen ska anges tid och plats för stämman, Vilka ärenden som ska förekomma på stämman, 
uppgift om den plats där de handlingar som anges i 12 § finns tillgängliga. 
Andra meddelanden skall genom styrelsens försorg bringas medlemarnas kännedom på samma sätt.
<p>
§14 Motioner<br>

En medlem kan genom motion väcka förslag rörande föreningens verksamhet. En motion, som ska behandlas på ordinarie stämma, ska vara styrelsen tillhanda senast under januari månad. 
Styrelsen ska bereda de motioner som kommit in och hålla dem tillgängliga för medlemmarna tillsammans med förvaltningsberättelsen, se ovan under 13 §.
<p>
§15 Dagordning vid stämma<br>

Vid ordinarie stämma ska följande ärenden behandlas:

    val av ordförande för stämman

    val av sekreterare för stämman

    val av två justerare

    styrelsens och revisorernas berättelser

    ansvarsfrihet för styrelsen

    framställningar från styrelsen eller motioner från medlemmarna

    ersättning till styrelsen och revisorerna

    styrelsens förslag till utgifts- och inkomststat samt debiteringslängd

    val av styrelse, styrelseordförande och suppleanter

    val av revisor och suppleant

    val av valberedning

    övriga frågor

    Meddelande av plats där protokollet från stämman hålls tillgängligt

Vid extra stämma ska ärendena under punkt 1, 2, 3, 6 och 13 behandlas.
<p>
§16 Fördelning av uppkommet överskott<br>

Om stämman beslutar om att fördela överskott som uppkommit ska fördelning ske efter medlemmarnas andelar i samfälligheten.
<p>
§17 Stämmobeslut<br>

Beslut fattas med acklamation om inte omröstning (votering) begärs.

Ifråga om omröstning m.m. gäller 48, 49, 51 och 52 § SFL. Vid en omröstning ska de omständigheter angående rösträtt, andelstal, ombud m.m. som har betydelse för röstresultatet antecknas i protokollet. Val ska ske med slutna sedlar om någon begär det.
<p>
§18 Flera verksamhetsgrenar <br>
- Skall omröstning ske i fråga som berör flera verksamhetsgrenar gemensamt har vid tillämpning
av huvudtalsmetoden varje medlem en röst även om han har del i flera verksamhetsgrenar.
Vid tillämpning av andelsmetoden skall i motsvarande fall varje medlems röstetal fram-
räknas på följande sätt. Först reduceras medlemens röstetal inom varje verksamhet
omröstningen avser. Därefter sammanlägges de reducerade röstetalen för varje medlem.
Verksamhetsgrenarnas andelar i verksamhet som är gemensam för samfälligheten(erna) utgör
lika andel för varje ingående anläggningssamfällighet.

<p>
§19 Protokollsjustering, tillgänglighållande<br>

Protokollet från stämman ska justeras och hållas tillgängligt för medlemmarna senast två veckor efter stämman.<p>

